/**
 * Custom renderer for fields containing sensitive information.
 */
class Lyranetwork_Payzen_Block_Adminhtml_System_Config_Field_Sensitive extends Mage_Adminhtml_Block_System_Config_Form_Field
{
    protected function _getElementHtml(Varien_Data_Form_Element_Abstract $element)
    {
        return str_replace('<input', '<input autocomplete="off"', parent::_getElementHtml($element));
    }
}
